    private void btnProblemasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProblemasActionPerformed
        // TODO add your handling code here:
        JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
        DlgBuscarProblemas dlg = new DlgBuscarProblemas(frame, true); // modal
        dlg.setLocationRelativeTo(this);
        dlg.setVisible(true); // esto bloquea hasta cerrar el diálogo

        // Después de cerrar, obtener el problema seleccionado
        Integer idSeleccionado = dlg.getProblemaSeleccionado();
        if (idSeleccionado != null) {
            this.problema = problemaController.obtenerProblema(idSeleccionado);
            this.idProblema = idSeleccionado;
            txtProblema.setText(this.problema.getDescripcion());

            cargarDisponibles();
            cargarAsignados();

        }
    }//GEN-LAST:event_btnProblemasActionPerformed

