    private void cargarDisponibles() {
        tblDisponibles.setModel(paController.obtenerTablaActivistasDisponibles(idProblema));
    }

    private void cargarAsignados() {
        tblAsignados.setModel(paController.obtenerTablaActivistasAsignados(idProblema));
    }

    private void asignar() {

        int fila = tblDisponibles.getSelectedRow();

        if (fila < 0) {

            JOptionPane.showMessageDialog(this, "Seleccione un activista");
            return;
        }

        int idActivista = (int) tblDisponibles.getValueAt(fila, 0);
        boolean exito = paController.asignarActivista(idProblema, idActivista);

        if (exito) {

            JOptionPane.showMessageDialog(this, "Se asignó un activista al problema correctamente.");
            cargarDisponibles();
            cargarAsignados();

        } else {

            JOptionPane.showMessageDialog(this, "Error al asignar");

        }
    }

    private void quitar() {

        int fila = tblAsignados.getSelectedRow();

        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un activista");
            return;
        }

        int idActivista = (int) tblAsignados.getValueAt(fila, 0);
        boolean exito = paController.eliminarAsignacion(idProblema, idActivista);

        if (exito) {

            JOptionPane.showMessageDialog(this, "Se eliminó un activista al problema correctamente.");
            cargarDisponibles();
            cargarAsignados();

        } else {

            JOptionPane.showMessageDialog(this, "Error al eliminar");

        }
    }

